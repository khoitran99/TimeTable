package entity;

/**
 *
 * @author khoi.tranvan
 */
public class Room {
    private int id;
    private int name;

    public Room(int id, int name) {
        this.id = id;
        this.name = name;
    }

    public Room() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }
    
    
}
